def get_length(obj):
    return len(obj)

# Example usage
string_example = "Hello, World!"
list_example = [1, 2, 3, 4, 5]
tuple_example = (10, 20, 30)
dict_example = {"a": 1, "b": 2, "c": 3}

print(f"The length of the string is: {get_length(string_example)}")
print(f"The length of the list is: {get_length(list_example)}")
print(f"The length of the tuple is: {get_length(tuple_example)}")
print(f"The length of the dictionary is: {get_length(dict_example)}")